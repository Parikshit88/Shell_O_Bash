#!/bin/bash
tput civis

Green=$'\e[1;32m'
Yellow=$'\e[1;33m'


while true
do
	clear


for ((i=1;i<11;i++)); 
do
	echo
done



	text=$"$Green$(date +%H):$Yellow$(date +%M)"
	term_width=$(tput cols)

	padding=$(( (term_width - ${#text}) / 2 ))

	padding_str=$(printf "%-${padding}s" " ")


	echo "${padding_str}${text}"
	sleep 60
done


































































































































































































































































































































































































